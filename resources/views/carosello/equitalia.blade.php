@section('fondo')
    <script>
        jQuery('document').ready(function () {

            jQuery('#inviasos').on('click', function (ele2) {
                ele2.preventDefault();
                var urlsos =   jQuery(this).attr('href');
                var chkArray = [];
                jQuery('input:checkbox[name=scelta]:checked').each(function() {
                    chkArray.push($(this).val());
                });
                var scelta;
                scelta = chkArray.join(', ') ;


                jQuery.ajax(
                    urlsos,
                    {
                        method: 'POST',
                        dataType: 'json',
                        data : {
                            '_token' : jQuery('#_token').val(),
                            'nomesos' : jQuery('#nomesos').val(),
                            'cognomesos' : jQuery('#cognomesos').val(),
                            'telefonosos' : jQuery('#telefonosos').val(),
                            'mailsos' : jQuery('#mailsos').val(),

                            'scelta' : scelta,
                        },
                        complete : function (resp2) {
                            console.log(resp2.responseText);
                            if(resp2.responseText == 1){
                                jQuery('#messsos').fadeIn(2000);
                                jQuery('#messsos').html("Messaggio inviato inviato");
                                jQuery('#messsos').fadeOut(4000);
                                jQuery('#nomesos').val("").attr("required", false);
                                jQuery('#cognomesos').val("").attr("required", false);
                                jQuery('#telefonosos').val("").attr("required", false);
                                jQuery('#mailsos').val("").attr("required", false);
                                jQuery('input:checkbox[name=scelta]:checked').attr("checked", false);

                            } else {
                                var cc = JSON.parse(resp2.responseText);
                                if(cc.errors.content){
                                    jQuery('#mess').html(cc.errors.content + " <br> messaggio non inviato");
                                }
                                if(cc.errors.mailfrom){
                                    jQuery('#mess').html(cc.errors.mailfrom + " <br> messaggio non inviato");
                                }
                                jQuery('#messsos').css("height", 80);
                                jQuery('#messsos').fadeIn(2000);
                                jQuery('#messsos').fadeOut(3000);
                            }
                        }
                    }
                )
            });
        });
    </script>
