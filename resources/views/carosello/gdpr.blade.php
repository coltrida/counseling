                                                            CHE COS’È innanzitutto il GDPR:
                                                            il Regolamento UE 2016/679 Il nuovo Regolamento (UE) 2016/679 per la Protezione dei Dati o GDPR (General Data Protection Regulation) determina le “linee guida” da adottare in materia di Protezione delle Persone Fisiche con riguardo al Trattamento dei Dati nonché alla libera circolazione di tali dati. È importante sottolineare il fatto che, bensì l’obbligo sia per tutte le aziende, i “Dati” a cui si riferisce il Regolamento sono quelli che riconducono o che si possono in qualche modo ricondurre a Persone Fisiche e non giuridiche (aziende).
                                                            RGPD (Regolamento Generale Protezione Dati) UE 2016/679
                                                            E che fine farà la “Legge Privacy” Dlgs 196/2003? Il  D.lgs 196 del 2003, meglio conosciuto come “Legge Privacy”, è stato abrogato definitivamente nel Marzo 2018. Sono comunque attualmente in corso di emanazione, da parte del Garante Privacy Italiano, nuove norme integrative che dovrebbero “raccordarsi” e fare “maggiore chiarezza” sul nuovo Regolamento Europeo.
                                                            Il Regolamento UE 2016/679 GDPR abroga completamente la vecchia Direttiva 95/46, dalla quale sono nate tutte le Leggi Privacy dei vari Stati Membri, tra cui anche la nostra Dlgs 196/2003. Trattandosi appunto di “Regolamento” e non di “Direttiva”, il GDPR non è soggetto stavolta a “recepimento” e non potrà quindi essere modificato dagli Stati Membri, né sul contenuto né, tanto meno, sulle date di applicazione essendo applicabile, fin dall’inizio, esattamente così com’è. Le aziende che avevano già adottato a suo tempo le misure previste nella “Legge Privacy” come, ad esempio, il DPS o Documento Programmatico della Sicurezza (poi abrogato dal 2013), saranno sicuramente avvantaggiate perché non dovranno partire da zero, se non altro come “cultura” da introdurre in azienda.

